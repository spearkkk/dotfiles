# Monitor density profiles
apply_density_profile() {
  local profile="${1:-comfortable}"

  case "$profile" in
    compact)
      BAR_HEIGHT=34
      DEFAULT_BG_HEIGHT=27
      FONT="Hack Nerd Font:BOLD:13.0"
      PILL_ICON_SIZE=15
      PILL_LABEL_SIZE=13
      INNER_PADDING=3
      OUTER_PADDING=6
      FRONT_APP_WIDTH=220
      FRONT_APP_MAX_CHARS=16
      ;;
    comfortable|*)
      BAR_HEIGHT=44
      DEFAULT_BG_HEIGHT=36
      FONT="Hack Nerd Font:BOLD:15.0"
      PILL_ICON_SIZE=18
      PILL_LABEL_SIZE=16
      INNER_PADDING=4
      OUTER_PADDING=8
      FRONT_APP_WIDTH=320
      FRONT_APP_MAX_CHARS=24
      ;;
  esac
}
