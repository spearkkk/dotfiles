# Shared typography
FONT="Hack Nerd Font:BOLD:15.0"
PILL_ICON_SIZE=18
PILL_LABEL_SIZE=16

# Shared surfaces/colors (expects colors.sh already sourced)
PILL_LABEL_COLOR="$TEXT_LIGHTEST"
